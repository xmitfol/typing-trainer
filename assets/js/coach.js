/**
 * coach.js — пост-урочный разбор + мотивация (Phase A, rule-based, без AI).
 *
 * Разбор строится по статистике ОДНОГО захода (точность/скорость/ритм + самая
 * частая ошибка по клавише). Мотивация — в стиле СОЛО (психология-первой),
 * голосом наставника. Глубокий анализ «слабые клавиши по сессиям» = отдельный
 * адаптивный движок (спек Аси, отложен).
 */
(function () {
    'use strict';

    // Разбор: что хорошо / над чем поработать. Возвращаем по 1-2 пункта.
    function analyze(s) {
        const good = [], improve = [];
        const acc = s.accuracy, rhythm = s.rhythm, wpm = s.wpm, target = s.targetWpm;

        if (acc >= 98) good.push('Точность почти идеальная — пальцы слушаются.');
        else if (acc >= 90) good.push('Хорошая точность.');
        else improve.push('Точность можно подтянуть — не спеши, бей по верной клавише.');

        if (rhythm != null) {
            if (rhythm >= 80) good.push('Ровный, уверенный ритм.');
            else if (rhythm < 50) improve.push('Ритм скачет — печатай на ровный счёт «раз-два».');
        }
        if (target && wpm >= target) good.push('Скорость уже на уровне цели.');

        if (s.topMistakeKey) {
            const k = s.topMistakeKey === ' ' ? 'пробел' : `«${s.topMistakeKey.toUpperCase()}»`;
            improve.push(`Чаще всего спотыкался на ${k} — стоит повторить.`);
        }

        if (!good.length) good.push('Дошёл до конца — это уже результат.');
        if (!improve.length) improve.push('Так держать — закрепляй темп и точность.');
        return { good: good.slice(0, 2), improve: improve.slice(0, 2) };
    }

    // Мотивация в стиле СОЛО, голосом наставника. passed=true — успех, false — поддержка.
    const MSG = {
        anna: {
            ok: ['«Точность бьёт скорость» — и у тебя она растёт. Спокойно, шаг за шагом.',
                 'Сравнивай себя не с другими, а с собой вчерашним. Сегодня ты лучше.'],
            up: ['Ничего страшного. Слепая печать — это про терпение, а не про спешку.',
                 'Каждая ошибка — это подсказка пальцам. Ещё заход — и станет легче.']
        },
        maxim: {
            ok: ['«Погибает не тот, кто устал, а кто остановился.» Ты не из тех, кто останавливается.',
                 'Каждое повторение — кирпичик мышечной памяти. Кладёшь ровно — молодец.'],
            up: ['Не торопись. Лучше медленно и точно, чем быстро и мимо.',
                 'Трудно? Значит, мышцы учатся. Лёгкое не тренирует.']
        },
        knopych: {
            ok: ['Вжух — и урок позади! Пальцы уже запоминают, где что живёт.',
                 'Кру-уто! Ещё пара уроков — и будешь печатать не глядя.'],
            up: ['Ошибки — это не провал, это разведка боем. Идём на второй заход!',
                 'Споткнулся? Бывает. Перезагрузка — и снова в бой.']
        },
        klavochka: {
            ok: ['Какой ты молодец! Пальчики стараются и всё лучше находят клавиши!',
                 'Ура! У тебя получается! Я в тебя верю!'],
            up: ['Чуть-чуть терпения — и буквы сами найдутся под пальцами.',
                 'Не расстраивайся! Попробуем ещё разок, не спеша.']
        }
    };

    function motivate(character, passed) {
        const c = MSG[character] || MSG.anna;
        const pool = passed ? c.ok : c.up;
        return pool[Math.floor(Math.random() * pool.length)];
    }

    window.coach = { analyze, motivate };
})();
